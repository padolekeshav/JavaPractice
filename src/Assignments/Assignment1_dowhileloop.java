package Assignments;

public class Assignment1_dowhileloop {

	public static void main(String[] args) {

		
// 10 to 1--> using for loop
		for(int i=10;i>=1;i--)
		{
			System.out.println(i);
		}
		
// using while loop : 
		int a=10;
		while(a>=1)
		{
			System.out.println(a);
			a--;
		}

		
//using do-while loop : 		
	}

}
